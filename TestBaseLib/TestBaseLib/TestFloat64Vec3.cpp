void TestFloat64Vec3(void)
{
	BeginTests();
	NumberInit();

	TestFloat64Vec3Add();
	TestFloat64Vec3Print();
	TestFloat64Vec3Magnitude();

	NumberKill();
	TestStatistics();
}

