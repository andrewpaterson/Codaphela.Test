#include "BaseLib/Int32Vec2.h"
#include "TestLib/Assert.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt32Vec2Add(void)
{
	SInt32Vec2 sia;
	SInt32Vec2 sib;

	sia.Init(1, 2);
	sib.Init(4, 3);

	sia += sib;

	AssertInt(5, sia.x);
	AssertInt(5, sia.y);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt32Vec2Print(void)
{
	SInt32Vec2	si;
	CChars		sz;

	si.Init(345, 1);
	sz.Init();
	si.Print(&sz);
	AssertString("[  345,    1]", sz.Text());
	sz.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt32Vec2Magnitude(void)
{
	SInt32Vec2	si;

	si.Init(345, 432);
	AssertInt(552, si.Magnitude());
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt32Vec2(void)
{
	BeginTests();
	NumberInit();

	TestInt32Vec2Add();
	TestInt32Vec2Print();
	TestInt32Vec2Magnitude();

	NumberKill();
	TestStatistics();
}

