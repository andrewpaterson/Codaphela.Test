//
//////////////////////////////////////////////////////////////////////////
void TestInt64Vec3Add(void)
{
	SInt64Vec3 sia;
	SInt64Vec3 sib;

	sia.Init(1, 2, 3);
	sib.Init(4, 3, 2);

	sia += sib;

	AssertLong(5, sia.x);
	AssertLong(5, sia.y);
	AssertLong(5, sia.z);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt64Vec3Print(void)
{
	SInt64Vec3	si;
	CChars		sz;

	si.Init(345, 0, 2897433423);
	sz.Init();
	si.Print(&sz);
	AssertString("[         345,           0,  2897433423]", sz.Text());
	sz.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt64Vec3Magnitude(void)
{
	SInt64Vec3	si;

	si.Init(20123405, 13459784, 478);
	AssertLong(24209857, si.Magnitude());
}


//////////////////////////////////////////////////////////////////////////
