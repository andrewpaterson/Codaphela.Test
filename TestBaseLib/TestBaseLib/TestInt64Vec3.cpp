//////////////////////////////////////////////////////////////////////////
void TestInt64Vec3(void)
{
	BeginTests();
	NumberInit();

	TestInt64Vec3Add();
	TestInt64Vec3Print();
	TestInt64Vec3Magnitude();

	NumberKill();
	TestStatistics();
}

