void TestIntVec4Add(void)
{
	SIntVec4 sia;
	SIntVec4 sib;

	sia.Init(1, 2, 3, 4);
	sib.Init(4, 3, 2, 1);

	sia += sib;

	AssertInt(5, sia.x);
	AssertInt(5, sia.y);
	AssertInt(5, sia.z);
	AssertInt(5, sia.w);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestIntVec4Print(void)
{
	SIntVec4	si;
	CChars		sz;

	si.Init(345, 0, 1397533873, -3);
	sz.Init();
	si.Print(&sz);
	AssertString("[         345,           0,  1397533873,          -3]", sz.Text());
	sz.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestIntVec4Magnitude(void)
{
	SIntVec4	si;

	si.Init(20123405, 13459784, 478, -3423454);
	AssertInt(24450710, si.Magnitude());
}


