//////////////////////////////////////////////////////////////////////////
void TestInt16Vec4(void)
{
	BeginTests();
	NumberInit();

	TestInt16Vec4Add();
	TestInt16Vec4Print();
	TestInt16Vec4Magnitude();

	NumberKill();
	TestStatistics();
}

