//////////////////////////////////////////////////////////////////////////
void TestInt16Vec4Add(void)
{
	SInt16Vec4 sia;
	SInt16Vec4 sib;

	sia.Init(1, 2, 3, 4);
	sib.Init(4, 3, 2, 1);

	sia += sib;

	AssertShort(5, sia.x);
	AssertShort(5, sia.y);
	AssertShort(5, sia.z);
	AssertShort(5, sia.w);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt16Vec4Print(void)
{
	SInt16Vec4	si;
	CChars		sz;

	si.Init(345, 0, 27973, -30001);
	sz.Init();
	si.Print(&sz);
	AssertString("[    345,      0,  27973, -30001]", sz.Text());
	sz.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt16Vec4Magnitude(void)
{
	SInt16Vec4	si;

	si.Init(20123, 13459, 478, -3423);
	AssertShort(24454, si.Magnitude());
}


//////////////////////////////////////////////////////////////////////////
//
