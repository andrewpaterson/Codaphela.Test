#include <string.h>
#include "FileIOTestObjects.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFileIOTest::Init(int iNumber)
{
	strcpy(msz, "Game of Thrones ");
	miNumber = iNumber;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFileIOTest::Kill(void)
{
	memset(msz, 0, 17);
	miNumber = -1;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CFileIOTest::IsOkay(int iNumber)
{
	if (iNumber != miNumber)
	{
		return FALSE;
	}
	return (strcmp(msz, "Game of Thrones ") == 0);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFileIOAlignedTest::Init(char bShunt)
{
	int		i;

	for (i = 0; i < 16; i++)
	{
		b[i] = (char)i + bShunt;
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFileIOAlignedTest::Kill(void)
{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CFileIOAlignedTest::IsOkay(char bShunt)
{
	int		i;

	for (i = 0; i < 16; i++)
	{
		if (b[i] != (char)i + bShunt)
		{
			return FALSE;
		}
	}
	return TRUE;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CFileIOAlignedTest::IsAligned(int iAlignment)
{
	size_t	pvLocation;
	int		iRemainder;

	pvLocation = (size_t)this;
	iRemainder = pvLocation % iAlignment;
	if (iRemainder != 0)
	{
		return FALSE;
	}
	return TRUE;
}

