#include <string.h>
#include "FileIOTestObjects.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFileIOTest::Init(int iNumber)
{
	strcpy(msz, "Game of Thrones ");
	miNumber = iNumber;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFileIOTest::Kill(void)
{
	memset(msz, 0, 17);
	miNumber = -1;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CFileIOTest::IsOkay(int iNumber)
{
	if (iNumber != miNumber)
	{
		return FALSE;
	}
	return (strcmp(msz, "Game of Thrones ") == 0);
}

