#include "BaseLib/Int64Vec2.h"
#include "TestLib/Assert.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt64Vec2Add(void)
{
	SInt64Vec2 sia;
	SInt64Vec2 sib;

	sia.Init(1, 2);
	sib.Init(4, 3);

	sia += sib;

	AssertLong(5, sia.x);
	AssertLong(5, sia.y);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt64Vec2Print(void)
{
	SInt64Vec2	si;
	CChars		sz;

	si.Init(345, 1);
	sz.Init();
	si.Print(&sz);
	AssertString("[  345,    1]", sz.Text());
	sz.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt64Vec2Magnitude(void)
{
	SInt64Vec2	si;

	si.Init(345, 432);
	AssertLong(552, si.Magnitude());
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt64Vec2(void)
{
	BeginTests();
	NumberInit();

	TestInt64Vec2Add();
	TestInt64Vec2Print();
	TestInt64Vec2Magnitude();

	NumberKill();
	TestStatistics();
}

