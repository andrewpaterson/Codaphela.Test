//////////////////////////////////////////////////////////////////////////
void TestIntVec3Add(void)
{
	SIntVec3 sia;
	SIntVec3 sib;

	sia.Init(1, 2, 3);
	sib.Init(4, 3, 2);

	sia += sib;

	AssertInt(5, sia.x);
	AssertInt(5, sia.y);
	AssertInt(5, sia.z);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestIntVec3Print(void)
{
	SIntVec3	si;
	CChars		sz;

	si.Init(345, 0, 1397533873);
	sz.Init();
	si.Print(&sz);
	AssertString("[         345,           0,  1397533873]", sz.Text());
	sz.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestIntVec3Magnitude(void)
{
	SIntVec3	si;

	si.Init(20123405, 13459784, 478);
	AssertInt(24209857, si.Magnitude());
}


//////////////////////////////////////////////////////////////////////////
