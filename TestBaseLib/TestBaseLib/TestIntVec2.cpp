#include "BaseLib/IntVec2.h"
#include "TestLib/Assert.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestIntVec2Add(void)
{
	SIntVec2 sia;
	SIntVec2 sib;

	sia.Init(1, 2);
	sib.Init(4, 3);

	sia += sib;

	AssertInt(5, sia.x);
	AssertInt(5, sia.y);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestIntVec2Print(void)
{
	SIntVec2	si;
	CChars		sz;

	si.Init(345, 1);
	sz.Init();
	si.Print(&sz);
	AssertString("[  345,    1]", sz.Text());
	sz.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestIntVec2Magnitude(void)
{
	SIntVec2	si;

	si.Init(20123405, 13459784);
	AssertInt(24209857, si.Magnitude());
}


//////////////////////////////////////////////////////////////////////////
//
