//////////////////////////////////////////////////////////////////////////
void TestIntVec2(void)
{
	BeginTests();
	NumberInit();

	TestIntVec2Add();
	TestIntVec2Print();
	TestIntVec2Magnitude();

	NumberKill();
	TestStatistics();
}

