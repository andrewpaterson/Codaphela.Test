#include "BaseLib/Int16Vec2.h"
#include "TestLib/Assert.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt16Vec2Add(void)
{
	SInt16Vec2 sia;
	SInt16Vec2 sib;

	sia.Init(1, 2);
	sib.Init(4, 3);

	sia += sib;

	AssertInt(5, sia.x);
	AssertInt(5, sia.y);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt16Vec2Print(void)
{
	SInt16Vec2	si;
	CChars		sz;

	si.Init(345, 1);
	sz.Init();
	si.Print(&sz);
	AssertString("[  345,    1]", sz.Text());
	sz.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt16Vec2Magnitude(void)
{
	SInt16Vec2	si;

	si.Init(325, 453);
	AssertInt(557, si.Magnitude());
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt16Vec2(void)
{
	BeginTests();
	NumberInit();

	TestInt16Vec2Add();
	TestInt16Vec2Print();
	TestInt16Vec2Magnitude();

	NumberKill();
	TestStatistics();
}

