void TestFloat2Add(void)
{
	SFloat32Vec2 sf4a;
	SFloat32Vec2 sf4b;

	sf4a.Init(1, 2);
	sf4b.Init(4, 3);

	sf4a += sf4b;

	AssertFloat(5, sf4a.x, 13);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestFloat2Print(void)
{
	SFloat32Vec2		sf;
	CChars		sz;

	sf.Init(345.342359f, 0.0002345f);
	sz.Init();
	sf.Print(&sz);
	AssertString("[ 345.34,   0.00]", sz.Text());
	sz.Kill();
}


