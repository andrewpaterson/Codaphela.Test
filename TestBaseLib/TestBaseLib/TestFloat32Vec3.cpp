//////////////////////////////////////////////////////////////////////////
void TestFloat32Vec3(void)
{
	BeginTests();
	NumberInit();

	TestFloat32Vec3Add();
	TestFloat32Vec3Print();
	TestFloat32Vec3Magnitude();

	NumberKill();
	TestStatistics();
}

