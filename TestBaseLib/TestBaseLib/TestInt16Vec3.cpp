//////////////////////////////////////////////////////////////////////////
void TestInt16Vec3Add(void)
{
	SInt16Vec3 sia;
	SInt16Vec3 sib;

	sia.Init(1, 2, 3);
	sib.Init(4, 3, 2);

	sia += sib;

	AssertInt(5, sia.x);
	AssertInt(5, sia.y);
	AssertInt(5, sia.z);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt16Vec3Print(void)
{
	SInt16Vec3	si;
	CChars		sz;

	si.Init(345, 0, 31953);
	sz.Init();
	si.Print(&sz);
	AssertString("[    345,      0,  31953]", sz.Text());
	sz.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt16Vec3Magnitude(void)
{
	SInt16Vec3	si;

	si.Init(2012, 1345, 478);
	AssertInt(2466, si.Magnitude());
}


//////////////////////////////////////////////////////////////////////////
//
