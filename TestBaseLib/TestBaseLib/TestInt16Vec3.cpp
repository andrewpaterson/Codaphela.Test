//////////////////////////////////////////////////////////////////////////
void TestInt16Vec3(void)
{
	BeginTests();
	NumberInit();

	TestInt16Vec3Add();
	TestInt16Vec3Print();
	TestInt16Vec3Magnitude();

	NumberKill();
	TestStatistics();
}

