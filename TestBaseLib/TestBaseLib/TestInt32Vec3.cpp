//////////////////////////////////////////////////////////////////////////
void TestInt32Vec3Add(void)
{
	SInt32Vec3 sia;
	SInt32Vec3 sib;

	sia.Init(1, 2, 3);
	sib.Init(4, 3, 2);

	sia += sib;

	AssertInt(5, sia.x);
	AssertInt(5, sia.y);
	AssertInt(5, sia.z);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt32Vec3Print(void)
{
	SInt32Vec3	si;
	CChars		sz;

	si.Init(345, 0, 1397533873);
	sz.Init();
	si.Print(&sz);
	AssertString("[         345,           0,  1397533873]", sz.Text());
	sz.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TestInt32Vec3Magnitude(void)
{
	SInt32Vec3	si;

	si.Init(20123405, 13459784, 478);
	AssertInt(24209857, si.Magnitude());
}


